#include "ft_printf.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
	g_vars.ret++;
}

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			ft_putchar(s[i]);
			i++;
		}
	}
}
